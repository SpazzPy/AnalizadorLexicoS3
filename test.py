# Importa la biblioteca math
import math

# Define una clase
class MiClase:
    # Define una funcion dentro de la clase
    def mi_funcion(self, x):
        # Utiliza una funcion de la biblioteca math
        return math.sqrt(x)

# Crea una instancia de la clase
mi_objeto = MiClase()

# Utiliza la funcion de la instancia de la clase
print(mi_objeto.mi_funcion(16))
